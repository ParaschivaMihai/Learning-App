package sample;

public class Decodificare {
    private String cnp;
    private String nrBonuri;
    private String nrBilete;

    public String getCnp() {
        return cnp;
    }

    public void setCnp(String cnp) {
        this.cnp = cnp;
    }

    public String getNrBonuri() {
        return nrBonuri;
    }

    public void setNrBonuri(String nrBonuri) {
        this.nrBonuri = nrBonuri;
    }

    public String getNrBilete() {
        return nrBilete;
    }

    public void setNrBilete(String nrBilete) {
        this.nrBilete = nrBilete;
    }
}
